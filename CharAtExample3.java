public class CharAtExample3{
public static void main(String[]args){
String str="Welcome to java traning";
int strLength=str.length();
//fetching first charecter
System.out.println("Charecter at 0 index is:"+str.charAt(0));
//the last charecter is persent at the string length-1 index
System.out.println("Charecter at last index is:"+str.charAt(strLength-1));
}
}